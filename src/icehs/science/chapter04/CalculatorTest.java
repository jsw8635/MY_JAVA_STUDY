package icehs.science.chapter04;

public class CalculatorTest {

	public static void main(String[] args) {
		int number1 = 20;
		int number2 = 7;
		
		int addResult = number1 + number2;
		int subtractResult = number1 - number2;
		int multiplyResult = number1 * number2;
		int divisionResult = number1 / number2;
		int reminderResult = number1 % number2;
		
		System.out.println("오챯叩 :" + addResult);
		System.out.println("된챯叩 :" + subtractResult);
		System.out.println("썼 써쎀 :" + multiplyResult);
		System.out.println("씱얋 써쎀 :" + divisionResult);
		System.out.println("씱쟵쵔 :" + reminderResult);

	}

}
